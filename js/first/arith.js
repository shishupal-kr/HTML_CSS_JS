//arithmetic operation

let a= 5;
let b = 4;

console.log("a+b=", a+b);
console.log("a-b=", a-b);
console.log("a*b=", a*b);
console.log("a/b=", a/b);
console.log("a%b=", a%b);

//unary operator

a = ++a;
console.log("a=",a);
b = --b;
console.log("a=",b);

//assignment operator
a+=4
console.log(a);
a-=4
console.log(a);
a*=4
console.log(a);

//comparison operator
console.log(a==b); //only chek num
console.log(a!=b);
console.log(a===b); //also check data type
console.log(a>b);
console.log(a>=b);
console.log(a<=b);

// logical operator
console.log(a=b && a>b)
console.log(a=b || a>b)
